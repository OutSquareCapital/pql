class Expression(DuckHandler):
    """Wrapper around Expression that uses SqlExpr instead of duckdb.Expression.

    This is a composition-based wrapper: it stores a ``_expr: Expression``
    and delegates all method calls, converting SqlExpr <-> duckdb.Expression
    at the boundary.
    """

    __slots__ = ()

    def __add__(self, other: IntoExpr) -> Self:
        """Add expr to self.

        Parameters:
                expr: The expression to add together with

        Returns:
                FunctionExpression: self '+' expr
        """
        return self._new(self.inner().__add__(into_duckdb(other)))

    def add(self, other: IntoExpr) -> Self:
        return self.__add__(other)

    def __and__(self, other: IntoExpr) -> Self:
        """Binary-and self together with expr.

        Parameters:
                expr: The expression to AND together with self

        Returns:
                FunctionExpression: self '&' expr
        """
        return self._new(self.inner().__and__(into_duckdb(other)))

    def and_(self, other: IntoExpr) -> Self:
        return self.__and__(other)

    def __div__(self, other: IntoExpr) -> Self:
        """Divide self by expr.

        Parameters:
                expr: The expression to divide by

        Returns:
                FunctionExpression: self '/' expr
        """
        return self._new(self.inner().__div__(into_duckdb(other)))

    def div(self, other: IntoExpr) -> Self:
        return self.__div__(other)

